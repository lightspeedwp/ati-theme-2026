<h2 class="wp-block-heading" style="font-weight:300">TOSCO ReGen Programme</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>ATI Holidays is also proud to highlight the wider movement towards regenerative tourism in Namibia. Through TOSCO's ReGen Namibia initiative, tourism businesses are helping shape a climate-action model that moves beyond traditional carbon credits and instead supports community and ecosystem resilience on the ground.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>This matters in Namibia, where tourism, conservation, and rural livelihoods are deeply connected. ReGen Namibia reflects a practical, collaborative approach rooted in the belief that travel can contribute to healthier landscapes and stronger communities.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>For ATI, this sits comfortably alongside the values already expressed across our business: encouraging guests to travel thoughtfully, supporting lodges with strong sustainability credentials, and helping ensure that tourism delivers meaningful long-term benefit.</p>
